streebog_s_transform_avx2:

    push    r14
    push    r15
                                # 2 pushes = 16 bytes, RSP already aligned
    sub     rsp, 8*32           # stack space for YMM backup

    # Save callee-saved YMM registers (ymm6-ymm15 are NOT callee-saved on Linux,
    # but we save them anyway to be safe if called from mixed code)
    vmovdqu [rsp + 0*32], ymm6
    vmovdqu [rsp + 1*32], ymm7
    vmovdqu [rsp + 2*32], ymm8
    vmovdqu [rsp + 3*32], ymm10
    vmovdqu [rsp + 4*32], ymm11
    vmovdqu [rsp + 5*32], ymm12
    vmovdqu [rsp + 6*32], ymm13
    vmovdqu [rsp + 7*32], ymm14

    lea     r14, [rip + SBOX]
    lea     r15, [rip + EXT]

    mov     r8,  rdi            # input
    mov     r9,  rsi            # output

    vmovdqu ymm0, [r8]
    vmovdqu ymm1, [r8 + 32]

    vmovdqu ymm7, [rip + MASK_0F]

    vpand   ymm2, ymm0, ymm7    # lower nibbles, first 32 bytes
    vpand   ymm3, ymm1, ymm7    # lower nibbles, second 32 bytes

    vpsrlw  ymm4, ymm0, 4
    vpsrlw  ymm5, ymm1, 4
    vpand   ymm4, ymm4, ymm7    # upper nibbles, first 32 bytes
    vpand   ymm5, ymm5, ymm7    # upper nibbles, second 32 bytes

    vpxor   ymm6, ymm6, ymm6
    vpxor   ymm8, ymm8, ymm8

    mov     ecx, 16

.Lloop:
    vmovdqu ymm10, [r14]

    vpshufb ymm11, ymm10, ymm2
    vpshufb ymm12, ymm10, ymm3

    vmovdqu ymm13, [r15]

    vpcmpeqb ymm14, ymm13, ymm4
    vpand    ymm11, ymm11, ymm14
    vpor     ymm6,  ymm6,  ymm11

    vpcmpeqb ymm14, ymm13, ymm5
    vpand    ymm12, ymm12, ymm14
    vpor     ymm8,  ymm8,  ymm12

    add     r14, 32
    add     r15, 32
    dec     ecx
    jnz     .Lloop

    vmovdqu [r9],      ymm6
    vmovdqu [r9 + 32], ymm8

    vmovdqu ymm6,  [rsp + 0*32]
    vmovdqu ymm7,  [rsp + 1*32]
    vmovdqu ymm8,  [rsp + 2*32]
    vmovdqu ymm10, [rsp + 3*32]
    vmovdqu ymm11, [rsp + 4*32]
    vmovdqu ymm12, [rsp + 5*32]
    vmovdqu ymm13, [rsp + 6*32]
    vmovdqu ymm14, [rsp + 7*32]

    add     rsp, 8*32
    pop     r15
    pop     r14
    vzeroupper
    ret

